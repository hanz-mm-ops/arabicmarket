  get() {
    return localStorage.getItem(this.KEY) || 'en';
  },

  set(lang) {
    localStorage.setItem(this.KEY, lang);
  },

  toggle() {
    const next = this.get() === 'en' ? 'ar' : 'en';
    this.set(next);
  },

  t(key) {
    const lang = this.get();
    return this.translations[lang][key] || this.translations['en'][key] || key;
  },

  isAr() {
    return this.get() === 'ar';
  },

  // Set text/html of element if it exists
  _setText(sel, key, html) {
    const els = typeof sel === 'string' ? document.querySelectorAll(sel) : [sel];
    els.forEach(el => {
      if (!el) return;
      const val = this.t(key);
      if (html) el.innerHTML = val;
      else el.textContent = val;
    });
  },

  _setPlaceholder(sel, key) {
    const els = typeof sel === 'string' ? document.querySelectorAll(sel) : [sel];
    els.forEach(el => {
      if (!el) return;
      el.placeholder = this.t(key);
    });
  },

  apply() {
    const lang = this.get();
    const isAr = lang === 'ar';

    // Set direction and lang
    document.documentElement.setAttribute('dir', isAr ? 'rtl' : 'ltr');
    document.documentElement.setAttribute('lang', lang);

    // Set Arabic font (SF Pro Arabic)
    if (isAr) {
      document.body.style.fontFamily = "'SF Pro Arabic', 'Inter', sans-serif";
      // Also update heading font
      document.documentElement.style.setProperty('--font', "'SF Pro Arabic', 'Space Grotesk', sans-serif");
      document.documentElement.style.setProperty('--body', "'SF Pro Arabic', 'Inter', sans-serif");
    } else {
      document.body.style.fontFamily = "";
      document.documentElement.style.setProperty('--font', "'Space Grotesk', sans-serif");
      document.documentElement.style.setProperty('--body', "'Inter', sans-serif");
    }

    // Update all elements with data-lang attribute
    document.querySelectorAll('[data-lang]').forEach(el => {
      const key = el.getAttribute('data-lang');
      const val = this.t(key);
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = val;
      } else {
        el.innerHTML = val;
      }
    });

    // Update lang toggle buttons — use the RIGHT font for the displayed text
    // When showing "عربي" (in EN mode), use SF Pro Arabic
    // When showing "EN" (in AR mode), use Space Grotesk
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.style.fontFamily = isAr
        ? "'Space Grotesk', sans-serif"
        : "'SF Pro Arabic', 'Space Grotesk', sans-serif";
    });
    document.querySelectorAll('.lang-btn-text').forEach(el => {
      el.textContent = this.t('lang_switch');
    });

    // Detect current page and apply page-specific translations
    const page = window.location.pathname.split('/').pop() || 'index.html';
    this._applyPage(page);
  },

  _applyPage(page) {
    // ─── NAV (shared across pages) ───
    const navLinks = document.querySelectorAll('.nav-links a');
    if (navLinks.length >= 1) navLinks[0].textContent = this.t('nav_collection1');
    if (navLinks.length >= 2) navLinks[1].textContent = this.t('nav_collection2');

    // ─── FOOTER (shared across pages) ───
    const footerBrandDesc = document.querySelector('.footer-brand-desc');
    if (footerBrandDesc) footerBrandDesc.innerHTML = this.t('footer_desc');

    const footerCols = document.querySelectorAll('.footer-col');
    if (footerCols.length >= 3) {
      // Shop column
      const shopH4 = footerCols[0].querySelector('h4');
      if (shopH4) shopH4.textContent = this.t('footer_shop');
      const shopLinks = footerCols[0].querySelectorAll('a');
      if (shopLinks[0]) shopLinks[0].textContent = this.t('nav_collection1');
      if (shopLinks[1]) shopLinks[1].textContent = this.t('nav_collection2');
      if (shopLinks[2]) shopLinks[2].textContent = this.t('footer_all');

      // Info column
      const infoH4 = footerCols[1].querySelector('h4');
      if (infoH4) infoH4.textContent = this.t('footer_info');
      const infoLinks = footerCols[1].querySelectorAll('a');
      if (infoLinks[0]) infoLinks[0].textContent = this.t('footer_sizing');
      if (infoLinks[1]) infoLinks[1].textContent = this.t('footer_shipping');
      if (infoLinks[2]) infoLinks[2].textContent = this.t('footer_returns');

      // Connect column
      const connectH4 = footerCols[2].querySelector('h4');
      if (connectH4) connectH4.textContent = this.t('footer_connect');
    }

    const footerBottom = document.querySelector('.footer-bottom');
    if (footerBottom) {
      const spans = footerBottom.querySelectorAll('span');
      if (spans[0]) spans[0].innerHTML = this.t('footer_rights');
      if (spans[1]) spans[1].textContent = this.t('footer_designed');
    }

    // Simple footer (store, product pages)
    const footerRight = document.querySelector('.footer-right');
    if (footerRight) footerRight.innerHTML = this.t('footer_rights');

    // ─── PAGE-SPECIFIC ───
    if (page === 'index.html' || page === '') {
      this._applyIndex();
    } else if (page === 'store.html') {
      this._applyStore();
    } else if (page === 'product.html') {
      this._applyProduct();
    } else if (page === 'checkout.html') {
      this._applyCheckout();
    } else if (page === 'login.html') {
      this._applyLogin();
    } else if (page === 'account.html') {
      this._applyAccount();
    }
  },

